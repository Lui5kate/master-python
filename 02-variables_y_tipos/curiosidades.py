mi_texto = "'Hola'"
mi_texto2 = "\"Mundo\""
mi_texto3 = '"Luis"'

texto_unido = mi_texto + " " + mi_texto2 + " " + mi_texto3

print(texto_unido)

# \n salto de línea
texto_unido = mi_texto + "\n" + mi_texto2
print(texto_unido)

# \t tabulador
texto_unido = mi_texto + "\t" + mi_texto2
print(texto_unido)

# \r borrar todo lo que está a la izquierda
texto_unido = mi_texto + "\r" + mi_texto2
print(texto_unido)

